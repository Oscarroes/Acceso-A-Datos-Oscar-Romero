var diaDeLaSemana = "domingo";

switch(diaDeLaSemana){
    case "lunes":
        console.log("Hoy es el primer día de la semana");
        break;
    case "martes":
        console.log("Hoy es el segundo día de la semana");
        break;
    case "miércoles":
        console.log("Hoy es el tercer día de la semana");
        break;
    case "jueves":
        console.log("Hoy es el cuarto día de la semana");
        break;
    case "viernes":
        console.log("Hoy es el quinto día de la semana");
        break;
    case "sábado":
        console.log("Hoy es el sexto día de la semana");
        break;
    case "domingo":
        console.log("Hoy es el séptimo día de la semana");
        break;
    default:
        console.log("No has escrito un día de la semana");
}



    